# but if one is provided with information showing what the answeer is,
# it is possible to verify the answer quickly.

# [NP stands for Nondeterministic PolynomialTime]
# NP Class of Problems:
    # The answer can be verified in PolynomialTime


### P versus NP Problem ###
# Q: Is every problem whose soultion is quickly verifiable also quickly solvable?
# The answer to P = NP question would determine:
    # Whether problems that can be verified in polynomial time can also be solved in polynomial time.

# IF P != NP [which is widely believed]
    # It would mean that there are problems in NP that are harder to compute than to verify:
        # They could not be Solved in PolynomialTime BUT TheAnswer could be Verified in PolynomialTime

